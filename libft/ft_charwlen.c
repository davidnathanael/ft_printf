int		ft_charwlen(wint_t x)
{
	size_t			j;

	j = 1;
	if (x <= 0x7F)
		return (j);
	if (x > 0x1FFFFF)
		return (-1);
	while (1)
	{
		if (!(x >>= 6))
			break ;
		j++;
	}
	return (j);
}
